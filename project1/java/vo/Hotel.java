package vo;

public class Hotel {
	
	private int hotel_idx;
	private int h_region1_idx;
	private int h_region2_idx;
	private String image;
	private String name;
	private String short_address;
	private String event;
	private int max_date;
	private String latitude;
	private String longitude;
	
	public Hotel() {;}

	public int getHotel_idx() {
		return hotel_idx;
	}

	public void setHotel_idx(int hotel_idx) {
		this.hotel_idx = hotel_idx;
	}

	public int getH_region1_idx() {
		return h_region1_idx;
	}

	public void setH_region1_idx(int h_region1_idx) {
		this.h_region1_idx = h_region1_idx;
	}

	public int getH_region2_idx() {
		return h_region2_idx;
	}

	public void setH_region2_idx(int h_region2_idx) {
		this.h_region2_idx = h_region2_idx;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getShort_address() {
		return short_address;
	}

	public void setShort_address(String short_address) {
		this.short_address = short_address;
	}

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}

	public int getMax_date() {
		return max_date;
	}

	public void setMax_date(int max_date) {
		this.max_date = max_date;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
	

}
